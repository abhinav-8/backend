//Operator
//typeof returns a string which contains the description of the type of the argument passed to it.

console.log(typeof -0); //number
console.log(typeof "This is a text"); //string
console.log(typeof true); //boolean
console.log(typeof undefined); //undefined
console.log(typeof null); //object (Corner case)
console.log(typeof NaN); //number 
console.log(typeof {name:"abhinav","age":23}); //object